            ip4prefixlen="20"
            ;;
        "2400:4050")
            # V6„Éó„É©„Çπ/OCN (2400:4050::/32)
            provider_type="v6plus_ocn"
            ipv4_base="153,240,0"
            ip6prefixlen="38"
