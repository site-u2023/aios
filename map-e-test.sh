# „Éó„É¨„Éï„Ç£„ÉÉ„ÇØ„Çπ„Å´ÂØæÂøú„Åô„ÇãIPv4„Éô„Éº„Çπ„Ç¢„Éâ„É¨„Çπ„Å®MAP-EË®≠ÂÆö„ÇíÂèñÂæó„Åô„ÇãÈñ¢Êï∞
get_base_mapping() {
    local prefix_head="$1"
    local provider_type=""
    local ipv4_base=""
    local ip6prefixlen=""
    local psidlen=""
    local offset=""
