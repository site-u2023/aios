    uci add_list firewall.@zone[1].network="$mape_iface"
    
    # Ë®≠ÂÆö„ÅÆ‰øùÂ≠ò„Å®ÈÅ©Áî®
    uci commit network
    uci commit firewall
    uci commit dhcp
    
    printf "%s\n" "$(color green "OCN MAP-EË®≠ÂÆö„ÇíÈÅ©Áî®„Åó„Åæ„Åó„Åü")"
    printf "%s: %s\n" "$(color cyan "IPv6„Éó„É¨„Éï„Ç£„ÉÉ„ÇØ„Çπ")" "$ipv6_prefix"
