# MAP-EÊÉÖÂ†±ÂèñÂæó„Å®Ë®àÁÆó (ash‰∫íÊèõ + bashÁâà„ÅÆÂ§âÊï∞Âêç„Å´Âêà„Çè„Åõ„Çã)
mape_mold() {
    debug_log "DEBUG" "Entering mape_mold() function" 

    local ip6_prefix_tmp
    # bashÊñáÂ≠óÂàóÁΩÆÊèõ„Çísed„Å´‰øÆÊ≠£
    ip6_prefix_tmp=$(echo "$NEW_IP6_PREFIX" | sed 's/::/:0::/')

    debug_log "DEBUG" "NEW_IP6_PREFIX=${NEW_IP6_PREFIX}, ip6_prefix_tmp=${ip6_prefix_tmp}" 

    # grep„Å®sed„Çí‰Ωø„Å£„ÅüHEXTETÊäΩÂá∫ (ash‰∫íÊèõ)
    local HEXTET0 HEXTET1 HEXTET2 HEXTET3
    if echo "$ip6_prefix_tmp" | grep -E '^([0-9a-f]{1,4}):([0-9a-f]{1,4}):([0-9a-f]{1,4}):([0-9a-f]{0,4})' >/dev/null; then
        local tmp
        tmp=$(echo "$ip6_prefix_tmp" | sed -e 's/:/ /g')
        local h0_str h1_str h2_str h3_str
        # read„Çí‰Ωø„Å£„Å¶Â§âÊï∞„Å´‰ª£ÂÖ• (ash‰∫íÊèõ)
        read -r h0_str h1_str h2_str h3_str _ <<EOF
$tmp
EOF
        # Á©∫ÊñáÂ≠ó„Çí0„Å´ÁΩÆÊèõ„Åó„ÄÅ16ÈÄ≤Êï∞„Çí10ÈÄ≤Êï∞„Å´Â§âÊèõ
        HEXTET0=$(printf %d "0x${h0_str:-0}")
        HEXTET1=$(printf %d "0x${h1_str:-0}")
        HEXTET2=$(printf %d "0x${h2_str:-0}")
        HEXTET3=$(printf %d "0x${h3_str:-0}")

        debug_log "DEBUG" "Parsed IPv6 prefix HEXTETs: HEXTET0=$HEXTET0, HEXTET1=$HEXTET1, HEXTET2=$HEXTET2, HEXTET3=$HEXTET3" 
    else
        # get_message Èñ¢Êï∞„ÅåÂ≠òÂú®„Åô„Çã„Å®‰ªÆÂÆö
        echo "$(get_message "prefix_not_recognized")"
        echo "$(get_message "direct_onu_connection")"
        echo "$(get_message "exiting")"
        debug_log "ERROR" "Failed to parse IPv6 prefix in mape_mold()"  # ERROR„É¨„Éô„É´„ÇÇdebug_log„Å´
        return 1
    fi

    # ÂêÑÁ®ÆË®àÁÆó (Ë§áÈõë„Å™„Éç„Çπ„ÉàË®àÁÆó„ÇíÂàÜÂâ≤) - bashÁâà„Å®Âêå„Åò
    local PREFIX31 PREFIX38
    local h0_mul=$(( HEXTET0 * 65536 ))    # 0x10000
    local h1_masked=$(( HEXTET1 & 65534 )) # 0xfffe
    PREFIX31=$(( h0_mul + h1_masked ))

    local h0_mul2=$(( HEXTET0 * 16777216 )) # 0x1000000
    local h1_mul=$(( HEXTET1 * 256 ))      # 0x100
    local h2_masked=$(( HEXTET2 & 64512 )) # 0xfc00
    local h2_shift=$(( h2_masked >> 8 ))
    PREFIX38=$(( h0_mul2 + h1_mul + h2_shift ))

    debug_log "DEBUG" "Calculated PREFIX31=$PREFIX31, PREFIX38=$PREFIX38" 

    # „Ç∞„É≠„Éº„Éê„É´Â§âÊï∞„Å®„Åó„Å¶Ë®≠ÂÆö„Åô„Çã„Éë„É©„É°„Éº„Çø„ÅÆÂàùÊúüÂåñ
    OFFSET=6  # „Éá„Éï„Ç©„É´„ÉàÂÄ§
    RFC=false # „Éá„Éï„Ç©„É´„ÉàÂÄ§ (bashÁâà„Å´Âêà„Çè„Åõ„Å¶false)
    IP6PREFIXLEN=""
    PSIDLEN=""
    IPV4=""
    PSID=0
    PORTS=""
    EALEN=""
    IP4PREFIXLEN=""
    IP6PFX=""
    BR=""
    CE_ADDR=""

    # „Éó„É¨„Éï„Ç£„ÉÉ„ÇØ„ÇπÂÄ§„Å´ÂØæÂøú„Åô„Çã„Éá„Éº„Çø„ÇíÂèñÂæó
    local prefix31_hex
    prefix31_hex=$(printf 0x%x "$PREFIX31")
    local prefix38_hex
    prefix38_hex=$(printf 0x%x "$PREFIX38")
    debug_log "DEBUG" "Processing prefix31=$prefix31_hex, prefix38=$prefix38_hex" 

    # IPv4„Ç¢„Éâ„É¨„Çπ„Å®ÂêÑÁ®Æ„Éë„É©„É°„Éº„Çø„ÅÆÊ±∫ÂÆö (bashÁâà/JSÁâà„Å®Âêå„ÅòÂÑ™ÂÖàÈ†ÜÂ∫è: 38_20 -> 38 -> 31)
    local octet1 octet2 octet3 octet4 octet
    if [ -n "$(get_ruleprefix38_20_value "$prefix38_hex")" ]; then
        octet="$(get_ruleprefix38_20_value "$prefix38_hex")"
        debug_log "DEBUG" "Matched ruleprefix38_20: $octet" 
        IFS=',' read -r octet1 octet2 octet3 <<EOF
$octet
EOF
        local temp1=$(( HEXTET2 & 960 ))    # 0x03c0
        local temp2=$(( temp1 >> 6 ))
        octet3=$(( octet3 | temp2 ))
        local temp3=$(( HEXTET2 & 63 ))     # 0x003f
        local temp4=$(( temp3 << 2 ))
        local temp5=$(( HEXTET3 & 49152 ))  # 0xc000
        local temp6=$(( temp5 >> 14 ))
        octet4=$(( temp4 | temp6 ))

        IPV4="${octet1}.${octet2}.${octet3}.${octet4}" # bashÁâà„ÅÆÂ§âÊï∞Âêç
        IP6PREFIXLEN=38
        PSIDLEN=6
        OFFSET=6 # ruleprefix38_20„Åß„ÅØ offset=6 „Çí‰ΩøÁî® (bashÁâà„ÇÇÂêåÊßò)
    elif [ -n "$(get_ruleprefix38_value "$prefix38_hex")" ]; then
        octet="$(get_ruleprefix38_value "$prefix38_hex")"
        debug_log "DEBUG" "Matched ruleprefix38: $octet" 
        IFS=',' read -r octet1 octet2 octet3 <<EOF
$octet
EOF
        local temp1=$(( HEXTET2 & 768 ))    # 0x0300
        local temp2=$(( temp1 >> 8 ))
        octet3=$(( octet3 | temp2 ))
        octet4=$(( HEXTET2 & 255 ))         # 0x00ff

        IPV4="${octet1}.${octet2}.${octet3}.${octet4}" # bashÁâà„ÅÆÂ§âÊï∞Âêç
        IP6PREFIXLEN=38
        PSIDLEN=8
        OFFSET=4
    elif [ -n "$(get_ruleprefix31_value "$prefix31_hex")" ]; then
        octet="$(get_ruleprefix31_value "$prefix31_hex")"
        debug_log "DEBUG" "Matched ruleprefix31: $octet" 
        IFS=',' read -r octet1 octet2 <<EOF
$octet
EOF
        octet2=$(( octet2 | (HEXTET1 & 1) )) # 0x0001
        local temp1=$(( HEXTET2 & 65280 ))  # 0xff00
        octet3=$(( temp1 >> 8 ))
        octet4=$(( HEXTET2 & 255 ))         # 0x00ff

        IPV4="${octet1}.${octet2}.${octet3}.${octet4}" # bashÁâà„ÅÆÂ§âÊï∞Âêç
        IP6PREFIXLEN=31
        PSIDLEN=8
        OFFSET=4
    else
        # get_message Èñ¢Êï∞„ÅåÂ≠òÂú®„Åô„Çã„Å®‰ªÆÂÆö
        echo "$(get_message "unsupported_prefix")"
        debug_log "ERROR" "No matching ruleprefix found in mape_mold()"  # ERROR„É¨„Éô„É´„ÇÇdebug_log„Å´
        return 1
    fi

    # PSIDË®àÁÆó (bashÁâà„Å®Âêå„Åò)
    if [ "$PSIDLEN" -eq 8 ]; then
        PSID=$(( (HEXTET3 & 65280) >> 8 )) # 0xff00
        debug_log "DEBUG" "PSID calculation for PSIDLEN=8: $PSID" 
    elif [ "$PSIDLEN" -eq 6 ]; then
        PSID=$(( (HEXTET3 & 16128) >> 8 )) # 0x3f00
        debug_log "DEBUG" "PSID calculation for PSIDLEN=6: $PSID" 
    else
        PSID=0 # „Éï„Ç©„Éº„É´„Éê„ÉÉ„ÇØ
        debug_log "DEBUG" "PSIDLEN ($PSIDLEN) is not 8 or 6, PSID set to 0" 
    fi

    # „Éù„Éº„ÉàÁØÑÂõ≤„ÅÆË®àÁÆó (bashÁâà„Å®Âêå„Åò)
    PORTS=""
    local AMAX=$(( (1 << OFFSET) - 1 ))
    debug_log "DEBUG" "Calculating port ranges: AMAX=$AMAX, OFFSET=$OFFSET, PSIDLEN=$PSIDLEN, PSID=$PSID" 

    local A
    for A in $(seq 1 "$AMAX"); do
        local shift_bits=$(( 16 - OFFSET ))
        local port_base=$(( A << shift_bits ))
        local psid_shift=$(( 16 - OFFSET - PSIDLEN ))
        # psid_shift „ÅåË≤†„Å´„Å™„Çã„Ç±„Éº„Çπ„ÇíÈÅø„Åë„Çã (PSIDLEN„Åå‰∏çÊòé„Å™Â†¥Âêà„Å™„Å©)
        if [ "$psid_shift" -lt 0 ]; then
            debug_log "ERROR" "Invalid calculation: psid_shift is negative ($psid_shift). Check OFFSET and PSIDLEN."  # ERROR„É¨„Éô„É´„ÇÇdebug_log„Å´
            psid_shift=0 # „Ç®„É©„Éº„ÇíÈò≤„Åê„Åü„ÇÅ0„Å´„Åô„Çã
        fi
        local psid_part=$(( PSID << psid_shift ))
        local port=$(( port_base | psid_part ))
        # port_range_size „Åå0„Å´„Å™„Çâ„Å™„ÅÑ„Çà„ÅÜ„Å´„Åô„Çã
        local port_range_size=$(( 1 << psid_shift ))
        if [ "$port_range_size" -le 0 ]; then
             debug_log "ERROR" "Invalid calculation: port_range_size is not positive ($port_range_size)."  # ERROR„É¨„Éô„É´„ÇÇdebug_log„Å´
             port_range_size=1 # „Ç®„É©„Éº„ÇíÈò≤„Åê„Åü„ÇÅ1„Å´„Åô„Çã
        fi
        local port_end=$(( port + port_range_size - 1 ))

        PORTS="${PORTS}${port}-${port_end}"

        if [ "$A" -lt "$AMAX" ]; then
            if [ $(( A % 3 )) -eq 0 ]; then
                PORTS="${PORTS}\\n" # ÊîπË°åÊñáÂ≠ó„Çí„Ç®„Çπ„Ç±„Éº„Éó„Åó„Å¶ËøΩÂä†
            else
                PORTS="${PORTS} "
            fi
        fi
    done
    # debug_log "DEBUG" "Calculated PORTS string (first 100 chars): $(echo "$PORTS" | cut -c 1-100)" # ÂøÖË¶Å„Å™„Çâ„Ç≥„É°„É≥„ÉàËß£Èô§

    # CE„Ç¢„Éâ„É¨„ÇπË®àÁÆóÁî®„ÅÆHEXTET„ÇíÊ∫ñÂÇô (bashÁâà„Å´Âêà„Çè„Åõ„Çã)
    local CE_HEXTET0 CE_HEXTET1 CE_HEXTET2 CE_HEXTET3 CE_HEXTET4 CE_HEXTET5 CE_HEXTET6 CE_HEXTET7
    CE_HEXTET0=$HEXTET0
    CE_HEXTET1=$HEXTET1
    CE_HEXTET2=$HEXTET2
    # hextet[3]=$((${hextet[3]} & 0xff00)) in bash script
    CE_HEXTET3=$(( HEXTET3 & 65280 )) # ‰∏ä‰Ωç„Éê„Ç§„Éà„ÅÆ„Åø‰øùÊåÅ (0xff00)

    # bashÁâà„ÅÆCE„Ç¢„Éâ„É¨„ÇπË®àÁÆó„É≠„Ç∏„ÉÉ„ÇØ (RFC„Éï„É©„Ç∞„ÅØfalseÂõ∫ÂÆö)
    if [ "$RFC" = "true" ]; then
        # „Åì„ÅÆ„Éñ„É≠„ÉÉ„ÇØ„ÅØRFC=false„ÅÆ„Åü„ÇÅÈÄöÂ∏∏ÂÆüË°å„Åï„Çå„Å™„ÅÑ„Åå„ÄÅbashÁâà„Å´Âêà„Çè„Åõ„Å¶Ë®òËø∞
        debug_log "DEBUG" "Calculating CE Address (RFC mode - unexpected)" 
        CE_HEXTET4=0
        CE_HEXTET5=$(( (octet1 << 8) | octet2 ))
        CE_HEXTET6=$(( (octet3 << 8) | octet4 ))
        CE_HEXTET7=$PSID
    else
        debug_log "DEBUG" "Calculating CE Address (Non-RFC mode)" 
        CE_HEXTET4=$octet1
        CE_HEXTET5=$(( (octet2 << 8) | octet3 ))
        CE_HEXTET6=$(( octet4 << 8 ))
        CE_HEXTET7=$(( PSID << 8 ))
    fi

    # CE„Ç¢„Éâ„É¨„ÇπÊñáÂ≠óÂàó„ÅÆÁîüÊàê (bashÁâà„ÅÆÂ§âÊï∞Âêç CE_ADDR)
    local CE0 CE1 CE2 CE3 CE4 CE5 CE6 CE7
    CE0=$(printf %04x "$CE_HEXTET0")
    CE1=$(printf %04x "$CE_HEXTET1")
    CE2=$(printf %04x "$CE_HEXTET2")
    CE3=$(printf %04x "$CE_HEXTET3")
    CE4=$(printf %04x "$CE_HEXTET4")
    CE5=$(printf %04x "$CE_HEXTET5")
    CE6=$(printf %04x "$CE_HEXTET6")
    CE7=$(printf %04x "$CE_HEXTET7")
    CE_ADDR="${CE0}:${CE1}:${CE2}:${CE3}:${CE4}:${CE5}:${CE6}:${CE7}" # bashÁâà„ÅÆÂ§âÊï∞Âêç
    debug_log "DEBUG" "Generated CE address (CE_ADDR): $CE_ADDR" 

    # EALEN„Å®„Éó„É¨„Éï„Ç£„ÉÉ„ÇØ„ÇπÈï∑„ÅÆË®àÁÆó (bashÁâà„Å®Âêå„Åò)
    EALEN=$(( 56 - IP6PREFIXLEN ))
    IP4PREFIXLEN=$(( 32 - (EALEN - PSIDLEN) ))
    debug_log "DEBUG" "EALEN=$EALEN, IP4PREFIXLEN=$IP4PREFIXLEN" 

    # IPv6„Éó„É¨„Éï„Ç£„ÉÉ„ÇØ„Çπ„ÅÆË®àÁÆó (bashÁâà„ÅÆÂ§âÊï∞Âêç IP6PFX)
    local IP6PFX0 IP6PFX1 IP6PFX2
    if [ "$IP6PREFIXLEN" -eq 38 ]; then
        # hextet2[2]=$(( ${hextet[2]} & 0xfc00)) in bash script
        local hextet2_2=$(( HEXTET2 & 64512 ))  # 0xfc00
        IP6PFX0=$(printf %x "$HEXTET0")
        IP6PFX1=$(printf %x "$HEXTET1")
        IP6PFX2=$(printf %x "$hextet2_2")
        IP6PFX="${IP6PFX0}:${IP6PFX1}:${IP6PFX2}" # bashÁâà„ÅÆÂ§âÊï∞Âêç
    elif [ "$IP6PREFIXLEN" -eq 31 ]; then
        # hextet2[1]=$(( ${hextet[1]} & 0xfffe )) in bash script
        local hextet2_1=$(( HEXTET1 & 65534 ))  # 0xfffe
        IP6PFX0=$(printf %x "$HEXTET0")
        IP6PFX1=$(printf %x "$hextet2_1")
        IP6PFX="${IP6PFX0}:${IP6PFX1}" # bashÁâà„ÅÆÂ§âÊï∞Âêç
    else
        IP6PFX="" # „Éï„Ç©„Éº„É´„Éê„ÉÉ„ÇØ
        debug_log "WARNING" "Could not determine IP6PFX for IP6PREFIXLEN=$IP6PREFIXLEN"  # WARNING„É¨„Éô„É´„ÇÇdebug_log„Å´
    fi
    debug_log "DEBUG" "Generated IPv6 prefix (IP6PFX): $IP6PFX" 

    # „Éñ„É≠„Éº„Éâ„Éê„É≥„Éâ„É´„Éº„Çø„Éº„Ç¢„Éâ„É¨„Çπ(BR/Peer)„ÅÆÂà§ÂÆö (bashÁâà„ÅÆÂ§âÊï∞Âêç BR)
    BR="" # bashÁâà„ÅÆÂ§âÊï∞Âêç
    # 10ÈÄ≤Êï∞„ÅßÊØîËºÉ
    # 0x24047a80 = 604240512, 0x24047a84 = 604240516, 0x24047a88 = 604240520
    # 0x240b0010 = 604512272, 0x240b0014 = 604512276
    # 0x240b0250 = 604512848, 0x240b0254 = 604512852
    if [ -n "$(get_ruleprefix38_20_value "$prefix38_hex")" ]; then
        BR="2001:380:a120::9"
    elif [ "$PREFIX31" -ge 604240512 ] && [ "$PREFIX31" -lt 604240516 ]; then
        BR="2001:260:700:1::1:275"
    elif [ "$PREFIX31" -ge 604240516 ] && [ "$PREFIX31" -lt 604240520 ]; then
        BR="2001:260:700:1::1:276"
    elif { [ "$PREFIX31" -ge 604512272 ] && [ "$PREFIX31" -lt 604512276 ]; } || \
         { [ "$PREFIX31" -ge 604512848 ] && [ "$PREFIX31" -lt 604512852 ]; }; then
        BR="2404:9200:225:100::64"
    fi
    debug_log "DEBUG" "Selected peer address (BR): $BR" 

    # „Ç∞„É≠„Éº„Éê„É´Â§âÊï∞„Å®„Åó„Å¶Ë®≠ÂÆö (mape_display, mape_config „ÅßÂèÇÁÖß„Åï„Çå„Çã)
    # IPV4, BR, IP6PFX, CE_ADDR, IP4PREFIXLEN, IP6PREFIXLEN, EALEN, PSIDLEN, OFFSET, PSID, PORTS

    debug_log "DEBUG" "Exiting mape_mold() function" 
    return 0
}

