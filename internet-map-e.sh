    uci set dhcp.wan6.ra='relay'
    uci set dhcp.wan6.dhcpv6='relay'
    uci set dhcp.wan6.ndp='relay'
    # uci set dhcp.wan6.ignore='1' # MAP-E„ÅåÊúâÂäπ„Å™Â†¥Âêà„ÄÅwan6„Åß„ÅÆDHCPv6Âá¶ÁêÜ„ÇíÁÑ°Ë¶ñ„Åô„Çã (OpenWrt 21.02‰ª•Èôç?)

    # WAN6„Ç§„É≥„Çø„Éº„Éï„Çß„Éº„ÇπË®≠ÂÆö (DHCPv6„ÇØ„É©„Ç§„Ç¢„É≥„Éà)
    uci set network.wan6.proto='dhcpv6'
    uci set network.wan6.reqaddress='try'  # „Ç¢„Éâ„É¨„ÇπË¶ÅÊ±Ç (ÂøÖÈ†à„Åß„ÅØ„Å™„ÅÑÂ†¥Âêà„ÇÇ)
    uci set network.wan6.reqprefix='auto' # „Éó„É¨„Éï„Ç£„ÉÉ„ÇØ„ÇπÂßîË≠≤Ë¶ÅÊ±Ç
    # uci set network.wan6.ip6prefix=${CE_ADDR}::/64 # „Åì„Çå„ÅØMAP-E„Ç§„É≥„Çø„Éº„Éï„Çß„Éº„Çπ„Åß„ÅØ„Å™„Åè„ÄÅwan6„Å´Ë®≠ÂÆö„Åô„Åπ„Åç„Åß„ÅØ„Å™„ÅÑ

