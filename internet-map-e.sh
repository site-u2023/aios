    uci set dhcp.lan.dhcpv6='relay'
    uci set dhcp.lan.ndp='relay'
    # uci set dhcp.lan.force='1' # force„ÅØÈÄöÂ∏∏‰∏çË¶Å„ÄÅÂïèÈ°å„Åå„ÅÇ„Çå„Å∞„Ç≥„É°„É≥„ÉàËß£Èô§

