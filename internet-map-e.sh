    echo ""
    echo "OpenWrt Configuration Values:" # "OpenWrtË®≠ÂÆöÂÄ§:"
    echo "  option peeraddr '$BR'" # BR„ÅåÁ©∫„ÅÆÂ†¥Âêà„ÇÇ„ÅÇ„Çã„Åü„ÇÅ„ÇØ„Ç©„Éº„Éà
    echo "  option ipaddr '$IPV4'"
    echo "  option ip4prefixlen '$IP4PREFIXLEN'"
    echo "  option ip6prefix '${IP6PFX}::'" # IP6PFX„ÅåÁ©∫„ÅÆÂ†¥Âêà„ÇÇ„ÅÇ„Çã„Åü„ÇÅ„ÇØ„Ç©„Éº„Éà
    echo "  option ip6prefixlen '$IP6PREFIXLEN'"
    echo "  option ealen '$EALEN'"
    echo "  option psidlen '$PSIDLEN'"
    echo "  option offset '$OFFSET'"
    # echo "  export LEGACY=1" # „Åì„Çå„ÅØuciË®≠ÂÆö„Åß„ÅØ„Å™„ÅÑ„Åü„ÇÅ„Ç≥„É°„É≥„Éà„Ç¢„Ç¶„ÉàÊé®Â•®

    # Âà©Áî®ÂèØËÉΩ„Å™„Éù„Éº„ÉàÁØÑÂõ≤„ÅÆË®àÁÆó (Ë°®Á§∫Áî®„ÄÅbashÁâà„Å´„ÅØ„Å™„ÅÑ„ÅåÊúâÁî®„Å™ÊÉÖÂ†±)
    local max_port_blocks=$(( (1 << OFFSET) )) # bashÁâà„ÅÆAmax+1Áõ∏ÂΩì„Å†„Åå„ÄÅ„É´„Éº„ÉóÂ§ñ„Å™„ÅÆ„ÅßÁõ¥Êé•Ë®àÁÆó
    local ports_per_block=$(( 1 << (16 - OFFSET - PSIDLEN) ))
    # bashÁâà„ÅÆ $lp „ÅØ Amax „Å™„ÅÆ„Åß„ÄÅ„Éñ„É≠„ÉÉ„ÇØÊï∞„ÅØ $lp = (1 << OFFSET) - 1 „Åß„ÅØ„Å™„Åè (1 << OFFSET)
    # „Åü„Å†„Åó„ÄÅ„Éù„Éº„ÉàÁØÑÂõ≤„ÅÆË®àÁÆóËá™‰Ωì„ÅØ A=1 „Åã„Çâ Amax „Åæ„Åß„Å™„ÅÆ„Åß„ÄÅÂà©Áî®ÂèØËÉΩ„Å™„Éñ„É≠„ÉÉ„ÇØÊï∞„ÅØ Amax ÂÄã
    local total_ports=$(( ports_per_block * ((1 << OFFSET) - 1) )) # A=1..AMax „ÅÆÂêàË®à„Éù„Éº„ÉàÊï∞
    local port_start=$(( (1 << (16 - OFFSET)) | (PSID << (16 - OFFSET - PSIDLEN)) )) # A=1 „ÅÆÊôÇ„ÅÆ„Éù„Éº„ÉàÈñãÂßãÂÄ§

    debug_log "DEBUG" "Port calculation for display: blocks=$max_port_blocks, ports_per_block=$ports_per_block, total_ports=$total_ports, first_port_start=$port_start" 

    echo ""
    echo "Port Information:" # "„Éù„Éº„ÉàÊÉÖÂ†±:"
    echo "  Available Ports: $total_ports" # "  Âà©Áî®ÂèØËÉΩ„Å™„Éù„Éº„ÉàÊï∞: $total_ports"
    # echo "  Base Port Start: $port_start" # Âü∫Êú¨„Éù„Éº„ÉàÈñãÂßãÂÄ§„ÅØË™§Ëß£„ÇíÊãõ„ÅèÂèØËÉΩÊÄß„Åå„ÅÇ„Çã„ÅÆ„Åß„Ç≥„É°„É≥„Éà„Ç¢„Ç¶„ÉàÊé®Â•®

    # „Éù„Éº„ÉàÁØÑÂõ≤„ÇíË°®Á§∫ (printf„Çí‰ΩøÁî®)
    echo ""
    echo "Port Ranges:" # "„Éù„Éº„ÉàÁØÑÂõ≤:"
    printf "%b\n" "$PORTS" # %b„Åß„Éê„ÉÉ„ÇØ„Çπ„É©„ÉÉ„Ç∑„É•„Ç®„Çπ„Ç±„Éº„Éó(\n)„ÇíËß£Èáà

    return 0
}

